    print(f"\n--- √âpoque {epoch}/{epochs} ---")
    
    # Entra√Ænement
    train_loss, train_acc = entrainer_une_epoque()
    
    # Test
    test_acc = tester()
    
    print(f"‚úÖ √âpoque {epoch} termin√©e:")
    print(f"   - Pr√©cision entra√Ænement: {train_acc:.2f}%")
